package com.xingyun.bean;

public class ProfileVideoItemBean {
	private int id;
	private String videourl;
	private String videohref;
	private String coverpath;
	private String ipadurl;
	private String vid;
	private int seq;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getVideourl() {
		return videourl;
	}
	public void setVideourl(String videourl) {
		this.videourl = videourl;
	}
	public String getVideohref() {
		return videohref;
	}
	public void setVideohref(String videohref) {
		this.videohref = videohref;
	}
	public String getCoverpath() {
		return coverpath;
	}
	public void setCoverpath(String coverpath) {
		this.coverpath = coverpath;
	}
	public String getIpadurl() {
		return ipadurl;
	}
	public void setIpadurl(String ipadurl) {
		this.ipadurl = ipadurl;
	}
	public String getVid() {
		return vid;
	}
	public void setVid(String vid) {
		this.vid = vid;
	}
	public int getSeq() {
		return seq;
	}
	public void setSeq(int seq) {
		this.seq = seq;
	}
}
